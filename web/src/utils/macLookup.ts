export const getMacVendor = (mac: string): string => {
  if (!mac || mac.length < 8) return '';
  const cleanMac = mac.trim().toUpperCase().replace(/-/g, ':');
  const oui = cleanMac.slice(0, 8);
  return OUI_MAP[oui] || '';
};
